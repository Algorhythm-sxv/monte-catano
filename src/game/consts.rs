#[cfg(test)]
mod tests {
    use crate::game::consts::*;

    #[test]
    fn verify_vertex_adjacency() {
        VERTEX_VERTICES.iter().enumerate().for_each(|(v, vs)| {
            assert!(
                vs.iter()
                    .take_while(|&&a| a != NONE)
                    .all(|a| VERTEX_VERTICES[*a].contains(&v)),
                "adjacency mismatch at vertex {v}: {vs:?}"
            )
        });
    }

    #[test]
    fn verify_hex_adjacency() {
        HEX_HEXES.iter().enumerate().for_each(|(h, hs)| {
            assert!(
                hs.iter()
                    .take_while(|&&h| h != NONE)
                    .all(|a| HEX_HEXES[*a].contains(&h)),
                "adjacency mismatch at hex {h}: {hs:?}"
            )
        });
    }

    #[test]
    fn verify_edge_adjacency() {
        EDGE_EDGES.iter().enumerate().for_each(|(e, es)| {
            assert!(
                es.iter()
                    .take_while(|&&e| e != NONE)
                    .all(|a| EDGE_EDGES[*a].contains(&e)),
                "adjacency mismatch at edge {e}: {es:?}"
            )
        });
    }
}
